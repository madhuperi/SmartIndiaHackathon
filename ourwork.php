          <h2>NSAP(IGNOAPS)</h2>   
			  <h3>What is IGNOAPS?</h3>
			  <p>The Indira Gandhi National Old Age Pension Scheme (IGNOAPS) is a non-contributory old age pension scheme that covers Indians who are 60 years and above and live below the poverty line. The pension scheme is part of the National Social Assistance Programme (NSAP) that was launched by the Ministry of Rural Development in August, 1995.</p>		  
			  
			  <h3>Eligibility</h3>
			  <p>All individuals above the age of 60 who live below the poverty line are eligible to apply for IGNOAPS.</p>     
			  			  
			  <h3>Pension amount</h3>
			  <p>All IGNOAPS beneficiaries aged 60-79 receive a monthly pension of Rs. 300 (Rs. 200 by central government and Rs. 100 by state government). Those 80 years and above receive a monthly pension amount of Rs.750.</p>		  
